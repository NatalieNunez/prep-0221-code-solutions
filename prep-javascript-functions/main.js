function addTwoNumbers(num1, num2) {
  return num1 + num2;
}

console.log(addTwoNumbers(4, 4));

function convertHoursToMinutes(number) {
  return number * 60;
}

console.log(convertHoursToMinutes(2));

function personalizeGreeting(name) {
  return `Hello ${name}!`;
}

console.log(personalizeGreeting('Tim D'));

function addAndMultiplyBy5(num1, num2) {
  return (num1 + num2) * 5;
}

console.log(addAndMultiplyBy5(2, 5));

function multiplyAndDivideBy5(num1, num2) {
  return (num1 * num2) / 5;
}

console.log(multiplyAndDivideBy5(10, 1));

function subtractTwoNumbers(num1, num2) {
  return num1 - num2;
}

console.log(subtractTwoNumbers(27, 10));

function getCircleCircumference(radius) {
  return 2 * 3.14 * radius;
}

console.log(getCircleCircumference(12));

function getFullName(firstName, lastName) {
  return `${firstName} ${lastName}`;
}

console.log(getFullName('Natalie', 'Nunez'));
